
import string

# Fornisce alcune costanti e funzioni per la manipolazione delle stringhe


string.ascii_letters #  stringa contenente tutte le lettere ASCII in minuscolo e maiuscolo.

string.ascii_lowercase #stringa contenente tutte le lettere ASCII in minuscolo.

string.ascii_uppercase #  stringa contenente tutte le lettere ASCII in maiuscolo.

string.digits # stringa contenente tutte le lettere ASCII in maiuscolo.

string.punctuation # stringa contenente tutti i caratteri di punteggiatura

string.hexdigits # stringa contenente tutti i caratteri esadecimali.
    
string.octdigits # stringa contenente tutti i caratteri ottali.

string.capwords() # restiuisce una nuova stringa con la prima lettera di ogni parola maiuscola