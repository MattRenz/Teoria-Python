import sys # funzione definita dentro un modulo

# IMPORTAZIONE MODULI ESTERNI 

sys.path # è una lista che contiene i percorsi in cui il programma cerca i moduli importati

sys.path.append() # è un metodo che consente di aggiungere un nuovo percorso alla lista sys.path.
# Ciò significa che il programma inizierà a cercare i moduli importanti anche nel nuovo percorso. 

# sys.path.append() non modifica permanentemente la lista sys.path. La modifica rimane valida solo per la durata dell'esecuzione del programma corrente


sys.exit()  # Interrompe l'esecuzione del programma, 
